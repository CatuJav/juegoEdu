    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            Ciencias cci= new Ciencias();
            conexion cc = new conexion();
            String id ="0";
            String txtusu= txtusuario.getText();
            String txtcontra="";
            char[] p = txtpass.getPassword();
            for (int i = 0; i < p.length; i++) {
                txtcontra += p[i];
            }
            Connection cn = cc.conectar();
            String sql ="SELECT * FROM usuarios;";
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            while(rs.next()){
                
                String usbd=rs.getString("nombre");
                String pasbd =rs.getString("contrasenia");
                if (usbd.equals(txtusu)&&pasbd.equals(txtcontra)) {
                     id = rs.getString("id_usuario");
                }
            }
            if (id.equals("0")) {
                JOptionPane.showMessageDialog(null, "No hay coincidencias");
            }else{
            
                cci.cargarProgreso(id);
                cci.cargarCiencias();
                cci.setVisible(true);
                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

